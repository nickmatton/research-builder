"""Decide whether a phase needs cloud GPU compute, and provision/teardown if so.

Flow per phase:
  1. ``needs_gpu(sub_spec)`` — cheap LLM call against the sub-spec markdown.
     Cached by phase_id so retries don't re-decide.
  2. If yes, ``provision(sub_spec, work_dir)``:
       - check the per-run BudgetLedger; if reservation would exceed the cap,
         bubble to the orchestrator's approval_callback (typically a click
         prompt) to ask the operator to raise the cap
       - generate ephemeral ed25519 keypair under work_dir/.cloud
       - register key with Lambda Cloud API
       - launch instance, wait until ready
       - drop work_dir/.cloud/env and work_dir/remote_run.sh
       - return ComputeHandle (whose ``teardown()`` terminates the instance
         and releases the budget entry)
  3. The execution loop runs the sub-agent normally. The sub-agent's prompt
     is augmented to know about remote_run.sh and the request_compute tool.
  4. Mid-phase the sub-agent can call ``upgrade(handle, ...)`` (via the
     request_compute MCP tool) to swap in a bigger box or extend hours; the
     same budget gate applies.
  5. ``ComputeHandle.teardown()`` is called from the loop's finally block.
"""

